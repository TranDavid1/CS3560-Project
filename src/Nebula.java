public class Nebula
{
    double size;
    double temperature;
    String type;

    public double getSize() {
        return size;
    }

    public void setSize( double s ) {
        size = s;
    }

    public double getTemperature() {
        return temperature;
    }

    public void setTemperature( double temp ) {
        temperature = temp;
    }

    public String getType() {
        return type;
    }

    public void setType( String t ) {
        type = t;
    }
}
