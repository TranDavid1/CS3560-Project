public class BlackHole
{
    double mass;
    double charge;
    double angularMomentum;
    String blackHoleType;

    public double getMass() {
        return mass;
    }

    public void setMass( double m ) {
        mass = m;
    }

    public double getCharge() {
        return charge;
    }

    public void setCharge( double c ) {
        charge = c;
    }

    public double getAngularMomentum() {
        return angularMomentum;
    }

    public void setAngularMomentum( double am ) {
        angularMomentum = am;
    }

    public String getBlackHoleType() {
        return blackHoleType;
    }

    public void setBlackHoleType( String type ) {
        blackHoleType = type;
    }
}
