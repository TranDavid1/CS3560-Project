public class Planet
{
    String type;
    int numberOfMoons;

    public String getType() {
        return type;
    }

    public void setType( String t ) {
        type = t;
    }

    public int getNumberOfMoons() {
        return numberOfMoons;
    }

    public void setNumberOfMoons( int n ) {
        numberOfMoons = n;
    }
}
