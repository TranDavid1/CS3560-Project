public class SolarSystem
{
    int numberOfPlanets;
    int numberOfStars;
    int numberOfComets;
    int numberOfAsteroids;

    public int getNumberOfPlanets() {
        return numberOfPlanets;
    }

    public void setNumberOfPlanets( int n ) {
        numberOfPlanets = n;
    }

    public int getNumberOfStars() {
        return numberOfStars;
    }

    public void setNumberOfStars( int n ) {
        numberOfStars = n;
    }

    public int getNumberOfComets() {
        return numberOfComets;
    }

    public void setNumberOfComets( int n ) {
        numberOfComets = n;
    }

    public int getNumberOfAsteroids() {
        return numberOfAsteroids;
    }

    public void setNumberOfAsteroids( int n ) {
        numberOfAsteroids = n;
    }
}
